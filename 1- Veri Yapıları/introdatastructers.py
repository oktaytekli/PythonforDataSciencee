# Sayılar: integer
x = 46
print(x)

# Sayılar: float
y = 10.3
print(y)

# Sayılar: complex
z= 2j+1
print(z)

# String
x = "Hello Blood"
print(x)

# Boolean (bool) True false ifadeleri

# Listeler
x = ["btc","eth","xrp"]
print(x)

# Sözlük (dictionary)
x = {
    "name": "Oktay",
    "age": "25"
}
print(x)

# Tuple
x = ("python","ml","ds")
print(x)

# Set (Dictionary gibi key value gibi bir olayı yok)
x = {"python","ml","ds"}
print(x)
#NOT: LİSTE, TUPLE, SET VE DICTIONARY VERİ YAPILARI AYNI ZAMANDA PYTHON COLLECTİONS(ARRAYS) OLARAK GEÇMEKTEDİR.